create table
  public.vacation_category (
    id bigint generated by default as identity,
    name text null,
    created_at timestamp with time zone not null default now(),
    image_url text null,
    slug text null,
    constraint vacation_category_pkey primary key (id)
  ) tablespace pg_default;

create table
  public.vacation_destination (
    id bigint generated by default as identity,
    name text null,
    image_url text null,
    description text null,
    video_url text null,
    island_id bigint null,
    province_id bigint null,
    created_at timestamp with time zone not null default now(),
    location text null,
    slug text null,
    content text null,
    header_image_url text null,
    url text null,
    constraint vacation_destination_pkey primary key (id),
    constraint vacation_destination_island_id_fkey foreign key (island_id) references vacation_island (id),
    constraint vacation_destination_province_id_fkey foreign key (province_id) references vacation_province (id)
  ) tablespace pg_default;

